void RegisterCors(IServiceCollection services)
{
    builder.Services.AddCors(options =>
    {
        options.AddPolicy(MyAllowSpecificOrigins,
            builder => builder
            //.WithOrigins(webUrl)
            .SetIsOriginAllowed(origin => true) // allow any origin
            .AllowAnyHeader()
            .AllowAnyMethod()
            .AllowCredentials()
            );
    });
}
